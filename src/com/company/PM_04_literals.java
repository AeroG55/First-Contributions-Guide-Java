package com.company;

public class PM_04_literals
{
    public static void main(String[] args)
    {
        byte age = 62;
        char ch = 'A';
        float f = 5.6f;
        double d1 = 7.44D;
        String a = "pm";
        System.out.println(age);
        System.out.println(f);
        System.out.println(d1);
        System.out.println(ch);
        System.out.println(a);
    }
}
  